    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");            
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/testdb", "root", "");
            String Query = "insert into users(sno, name, address, gender, knowledge, subject) values(?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(Query) ;
            pst.setString(1, sno.getText());
            pst.setString(2, name.getText());
            pst.setString(3, address.getText());

            if(male.isSelected()) {
                gender="male";

            }

            if(female.isSelected()) {
                gender="female";

            }
            pst.setString(4, gender);
            String knowledge= "";
            if(coreJava.isSelected()){
                knowledge+=coreJava.getText()+" ";
            }
            if(python.isSelected()){
                knowledge+=python.getText()+" ";
            }
            pst.setString(5,knowledge);
            String course;
            course=subject.getSelectedItem().toString();
            pst.setString(6,course);
            pst.executeUpdate();
            DefaultTableModel model = (DefaultTableModel)jTable1_Display_User.getModel();
            model.setRowCount(0);
            show_user();
            JOptionPane.showMessageDialog(null, "insert successfull ");

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);

        }

    }//GEN-LAST:event_saveBtnActionPerformed

