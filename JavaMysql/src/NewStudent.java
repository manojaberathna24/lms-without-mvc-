    public ArrayList<User>userList(){
        ArrayList<User> usersList = new ArrayList<>();
        
    try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/testdb", "root", "");
            String Query1="select * from users";
            Statement st= con.createStatement();
            ResultSet rs= st.executeQuery(Query1);
            User user;
            while(rs.next()){
                user=new User(rs.getInt("sno"), rs.getString("name"), rs.getString("address"),  rs.getString("gender"),  rs.getString("knowledge"),  rs.getString("subject"));
                usersList.add(user);
                
                
                    
            }
            
            
            
           }
            catch(Exception e){
            JOptionPane.showMessageDialog(null, e);

        }
        return usersList;
        
    }
    public void show_user(){
        ArrayList<User> list = userList();
        DefaultTableModel model = (DefaultTableModel)jTable1_Display_User.getModel();
        Object[] row = new Object[6];
        for(int i=0;i<list.size();i++){
            row[0] = list.get(i).getsno();
            row[1] = list.get(i).getname();
            row[2] = list.get(i).getaddress();
            row[3] = list.get(i).getgender();
            row[4] = list.get(i).knowledge();
            row[5] = list.get(i).getsubject();
            model.addRow(row);
        }
        
        
        
    }
    
    
